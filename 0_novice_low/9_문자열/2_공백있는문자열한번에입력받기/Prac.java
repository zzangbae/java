import java.util.*;

public class Prac {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        // 엔터전까지 문자열 받음(enter)
        String s1 = sc.nextLine();
        // 공백전까지 문자열 받음(space, enter)
        String s2 = sc.next();

        // 똑같이 hello world를 넣어봤다
        System.out.println(s1); // hello world
        System.out.println(s2); // hello
    }
}