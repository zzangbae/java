import java.util.*;

/**
 * 문자열 리스트 관리
 */
public class Prac {
    public static void main(String[] args) {
        String[] arr = new String[]{"banana", "apple", "pizza"};

        System.out.println(arr[0]); // banana
        System.out.println(arr[0].length());    // 6
        System.out.println(arr[0].charAt(0));   // b
        System.out.println(arr[1].charAt(1));   // p
    }    
}
